package input;

public class InputEvents {
	
	public static boolean isUp() {
		return Input.isUp();
	}
	
	public static boolean isDown() {
		return Input.isDown();
	}
	
	public static boolean isLeft() {
		return Input.isLeft();
	}
	
	public static boolean isRight() {
		return Input.isRight();
	}
	
}
